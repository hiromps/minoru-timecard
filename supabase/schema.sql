-- 社員
CREATE TABLE IF NOT EXISTS public.employees (
    id              bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    employee_id     text NOT NULL UNIQUE,                 -- 業務キー（打刻・FK 参照先）
    name            text NOT NULL,
    department      text,
    work_start_time time DEFAULT '09:00:00',              -- 所定始業（JST の時刻）
    work_end_time   time DEFAULT '17:00:00',              -- 所定終業（JST の時刻）
    created_at      timestamptz DEFAULT now(),
    updated_at      timestamptz DEFAULT now(),
    is_active       boolean DEFAULT true,
    created_by      uuid,
    updated_by      uuid
);

-- 打刻記録
CREATE TABLE IF NOT EXISTS public.time_records (
    id               bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    employee_id      text NOT NULL REFERENCES public.employees(employee_id) ON DELETE CASCADE,
    record_date      date NOT NULL,                       -- JST の勤務日
    clock_in_time    timestamptz,                         -- UTC 保存
    clock_out_time   timestamptz,                         -- UTC 保存
    status           text DEFAULT '通常',
    work_hours       numeric DEFAULT 0,
    created_at       timestamptz DEFAULT now(),
    updated_at       timestamptz DEFAULT now(),
    ip_address       inet,
    user_agent       text,
    is_manual_entry  boolean DEFAULT false,
    approved_by      uuid,
    overtime_minutes integer NOT NULL DEFAULT 0,          -- 残業（分）= 退勤 - 所定終業、0以上
    is_direct_work   boolean NOT NULL DEFAULT false,      -- 直行・直帰（遅刻/早退/残業判定を無効化）
    CONSTRAINT time_records_status_check CHECK (
        status = ANY (ARRAY['通常','遅刻','早退','残業','遅刻・早退','遅刻・残業','設定エラー'])
    ),
    CONSTRAINT check_work_hours CHECK (work_hours >= 0 AND work_hours <= 24),
    CONSTRAINT check_clock_times CHECK (
        clock_out_time IS NULL OR clock_in_time IS NULL OR clock_out_time > clock_in_time
    )
);

COMMENT ON COLUMN public.time_records.overtime_minutes IS '残業時間（分）。退勤時刻 - 所定退勤時刻(work_end_time)。0以上。';
COMMENT ON COLUMN public.time_records.is_direct_work   IS '直行・直帰モードで打刻された記録か。trueの場合は遅刻/早退/残業の自動判定を無効化する。';

-- 管理者プロファイル（Supabase Auth と連携）
CREATE TABLE IF NOT EXISTS public.admin_profiles (
    id                    uuid PRIMARY KEY REFERENCES auth.users(id),
    name                  text,
    is_active             boolean DEFAULT true,
    created_at            timestamptz DEFAULT now(),
    updated_at            timestamptz DEFAULT now(),
    email                 text,
    role                  text DEFAULT 'viewer',
    last_login            timestamptz,
    failed_login_attempts integer DEFAULT 0,
    locked_until          timestamptz,
    CONSTRAINT check_admin_role CHECK (role = ANY (ARRAY['super_admin','admin','viewer']))
);

-- 監査ログ
CREATE TABLE IF NOT EXISTS public.audit_logs (
    id          bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    table_name  text NOT NULL,
    record_id   text NOT NULL,
    action      text NOT NULL,
    old_values  jsonb,
    new_values  jsonb,
    user_id     uuid,
    ip_address  inet,
    user_agent  text,
    created_at  timestamptz DEFAULT now(),
    reason      text,
    CONSTRAINT audit_logs_action_check CHECK (action = ANY (ARRAY['INSERT','UPDATE','DELETE','SELECT']))
);

-- セッション管理
CREATE TABLE IF NOT EXISTS public.user_sessions (
    id            uuid PRIMARY KEY DEFAULT gen_random_uuid(),
    user_id       uuid NOT NULL,
    ip_address    inet NOT NULL,
    user_agent    text,
    is_active     boolean DEFAULT true,
    expires_at    timestamptz NOT NULL,
    created_at    timestamptz DEFAULT now(),
    last_activity timestamptz DEFAULT now()
);

