-- =====================================================================
-- 1. employees — 社員マスタ
-- =====================================================================
CREATE TABLE IF NOT EXISTS public.employees (
    id              bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, -- ⚠️推定: TS では number。serial/identity のいずれか
    employee_id     text NOT NULL UNIQUE,          -- 業務キー（例 "001"）。time_records から参照される
    name            text NOT NULL,
    department      text,                           -- NULL 許容
    work_start_time text NOT NULL DEFAULT '09:00',  -- ⚠️推定: 型は text か time。JST "HH:MM"。診断SQLが ::time でキャストしている点から text の可能性が高い
    work_end_time   text NOT NULL DEFAULT '17:00',  -- ⚠️推定: 同上
    created_at      timestamptz NOT NULL DEFAULT now(),
    updated_at      timestamptz NOT NULL DEFAULT now()
);

COMMENT ON TABLE  public.employees IS '社員マスタ。個別の所定勤務時間を保持する。';
COMMENT ON COLUMN public.employees.employee_id     IS '業務上の社員ID（text, 例 "001"）。time_records.employee_id と対応。';
COMMENT ON COLUMN public.employees.work_start_time IS '所定始業時刻（JST, "HH:MM"）。';
COMMENT ON COLUMN public.employees.work_end_time   IS '所定終業時刻（JST, "HH:MM"）。';

-- =====================================================================
-- 2. time_records — 打刻記録
-- =====================================================================
CREATE TABLE IF NOT EXISTS public.time_records (
    id               bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, -- ⚠️推定: TS では number
    employee_id      text NOT NULL,          -- employees.employee_id に対応（⚠️推定: 実FK制約の有無は要確認。退職者の孤児レコードが存在しうる）
    record_date      date NOT NULL,          -- JST の暦日
    clock_in_time    timestamptz,            -- 出勤（UTC保存）。NULL 可
    clock_out_time   timestamptz,            -- 退勤（UTC保存）。NULL 可
    status           text NOT NULL DEFAULT '通常',  -- 下記 CHECK 参照
    work_hours       numeric NOT NULL DEFAULT 0,    -- 実労働時間（時間）。昼休憩控除後
    overtime_minutes integer NOT NULL DEFAULT 0,    -- 残業（分）。add-overtime-minutes.sql で追加
    is_direct_work   boolean NOT NULL DEFAULT false, -- 直行直帰。true なら遅刻/早退/残業判定を無効化
    is_manual_entry  boolean NOT NULL DEFAULT false, -- ⚠️推定: 手動入力フラグ。getAllTimeRecords が参照
    approved_by      text,                   -- ⚠️推定: 承認者。型は text か uuid。getAllTimeRecords が参照
    notes            text,                   -- ⚠️推定: admin_create_time_record RPC のみが参照
    created_by_admin boolean NOT NULL DEFAULT false, -- ⚠️推定: admin_create_time_record RPC のみが参照
    created_at       timestamptz NOT NULL DEFAULT now(),
    updated_at       timestamptz NOT NULL DEFAULT now(),

    -- 退勤 > 出勤（負の勤務時間を禁止）。workTimeUtils の判定とも整合
    CONSTRAINT check_clock_times CHECK (
        clock_out_time IS NULL
        OR clock_in_time IS NULL
        OR clock_out_time > clock_in_time
    )
);

-- 1日1レコード。コード上「部分ユニーク索引」と表現されているため部分索引で復元。
-- ⚠️推定: 実 DB では通常の UNIQUE(employee_id, record_date) の可能性もある。要突合。
CREATE UNIQUE INDEX IF NOT EXISTS uq_time_records_employee_date
    ON public.time_records (employee_id, record_date);

-- 参照系クエリ用の索引（⚠️推定: 実在有無は要確認）
CREATE INDEX IF NOT EXISTS idx_time_records_record_date  ON public.time_records (record_date);
CREATE INDEX IF NOT EXISTS idx_time_records_employee_id  ON public.time_records (employee_id);

COMMENT ON TABLE  public.time_records IS '打刻記録。1社員・1日1レコード。';
COMMENT ON COLUMN public.time_records.clock_in_time    IS '出勤時刻（timestamptz, UTC保存）。';
COMMENT ON COLUMN public.time_records.clock_out_time   IS '退勤時刻（timestamptz, UTC保存）。';
COMMENT ON COLUMN public.time_records.overtime_minutes IS '残業時間（分）。退勤時刻 - 所定退勤時刻(work_end_time)。0以上。';
COMMENT ON COLUMN public.time_records.is_direct_work   IS '直行直帰。true の場合、遅刻/早退/残業判定を無効化し残業0とする。';

