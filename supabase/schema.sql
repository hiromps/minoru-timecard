-- 8.6 管理者による打刻作成
-- ⚠️注意: この RPC は p_employee_id を UUID として受け、time_records.employee_id
--         (text) に挿入する。実運用のフロントは呼び出しておらず、型不整合の疑い。
--         また notes / created_by_admin 列を参照する（time_records の推定列）。
CREATE OR REPLACE FUNCTION public.admin_create_time_record(
    p_employee_id  UUID,
    p_record_date  DATE,
    p_clock_in_time  TIMESTAMPTZ DEFAULT NULL,
    p_clock_out_time TIMESTAMPTZ DEFAULT NULL,
    p_notes TEXT DEFAULT NULL
)
RETURNS UUID AS $$
DECLARE
    v_record_id UUID;
BEGIN
    IF NOT public.is_admin_with_write_access() THEN
        RAISE EXCEPTION 'Permission denied: Admin access required';
    END IF;

    INSERT INTO public.time_records (
        employee_id, record_date, clock_in_time, clock_out_time, notes, created_by_admin
    )
    VALUES (
        p_employee_id, p_record_date, p_clock_in_time, p_clock_out_time, p_notes, true
    )
    RETURNING id INTO v_record_id;

    RETURN v_record_id;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER
SET search_path = public;

-- 8.7 管理者による打刻削除
CREATE OR REPLACE FUNCTION public.admin_delete_time_record(p_record_id UUID)
RETURNS boolean AS $$
BEGIN
    IF NOT public.is_admin_with_write_access() THEN
        RAISE EXCEPTION 'Permission denied: Admin access required';
    END IF;

    DELETE FROM public.time_records WHERE id = p_record_id;
    RETURN FOUND;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER
SET search_path = public;

-- 8.8 打刻記録の原子的修正（delete + insert を単一トランザクションで）
-- ⚠️重大な推定: この関数の定義 SQL はリポジトリに存在しない。以下は
--   src/lib/database.ts の呼び出し（引数名・型・戻り値の id 参照）から復元した
--   ベストエフォート版。実 DB の定義と必ず突合すること。
--   ・p_employee_id は業務キー text（"001"）として渡される（UUID ではない）。
--   ・(employee_id, record_date) の既存行を削除し、新しい行を1件挿入して返す。
--   ・insert 失敗時は delete もロールバックされ、記録消失を防ぐ。
CREATE OR REPLACE FUNCTION public.correct_time_record(
    p_employee_id      TEXT,
    p_record_date      DATE,
    p_clock_in_time    TIMESTAMPTZ,
    p_clock_out_time   TIMESTAMPTZ,
    p_work_hours       NUMERIC,
    p_overtime_minutes INTEGER,
    p_status           TEXT,
    p_is_direct_work   BOOLEAN DEFAULT false
)
RETURNS public.time_records AS $$
DECLARE
    v_row public.time_records;
BEGIN
    -- 同一社員・同一日の既存レコードを削除
    DELETE FROM public.time_records
     WHERE employee_id = p_employee_id
       AND record_date = p_record_date;

    -- 新しいレコードを挿入
    INSERT INTO public.time_records (
        employee_id, record_date, clock_in_time, clock_out_time,
        work_hours, overtime_minutes, status, is_direct_work
    )
    VALUES (
        p_employee_id, p_record_date, p_clock_in_time, p_clock_out_time,
        p_work_hours, p_overtime_minutes, p_status, p_is_direct_work
    )
    RETURNING * INTO v_row;

    RETURN v_row;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER
SET search_path = public;

