-- updated_at 自動更新用（※実 DB ではどのテーブルにもトリガー未設置）
CREATE OR REPLACE FUNCTION public.update_updated_at_column()
RETURNS trigger
LANGUAGE plpgsql
SET search_path TO 'public'
AS $function$
BEGIN
    NEW.updated_at = NOW();
    RETURN NEW;
END;
$function$;

-- 監査トリガー用関数
-- ⚠️ 既知の不具合: 実 audit_logs には old_data/new_data 列が無く（正しくは
--    old_values/new_values）、この関数は壊れている。実 DB ではどのテーブルにも
--    トリガーとして設置されていないため発火せず、実害はない（デッドコード）。
--    新規構築時にトリガーを張る場合は列名を old_values/new_values に修正すること。
CREATE OR REPLACE FUNCTION public.audit_trigger_function()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path TO 'public'
AS $function$
BEGIN
    INSERT INTO public.audit_logs (
        table_name, action, old_data, new_data, user_id, created_at   -- ⚠️ old_data/new_data は実在しない列
    )
    VALUES (
        TG_TABLE_NAME,
        TG_OP,
        CASE WHEN TG_OP = 'DELETE' OR TG_OP = 'UPDATE' THEN row_to_json(OLD) ELSE NULL END,
        CASE WHEN TG_OP = 'INSERT' OR TG_OP = 'UPDATE' THEN row_to_json(NEW) ELSE NULL END,
        auth.uid(),
        NOW()
    );
    IF TG_OP = 'DELETE' THEN
        RETURN OLD;
    ELSE
        RETURN NEW;
    END IF;
END;
$function$;

-- 期限切れセッションの無効化・古いセッションの削除
CREATE OR REPLACE FUNCTION public.cleanup_expired_sessions()
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path TO 'public'
AS $function$
BEGIN
    UPDATE public.user_sessions
    SET is_active = false
    WHERE expires_at < NOW() AND is_active = true;

    DELETE FROM public.user_sessions
    WHERE is_active = false AND created_at < NOW() - INTERVAL '30 days';
END;
$function$;

-- ログイン失敗処理（5回以上で1時間ロック）
CREATE OR REPLACE FUNCTION public.handle_failed_login(user_email text)
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path TO 'public'
AS $function$
BEGIN
    UPDATE public.admin_profiles
    SET
        failed_login_attempts = failed_login_attempts + 1,
        locked_until = CASE
            WHEN failed_login_attempts >= 5 THEN NOW() + INTERVAL '1 hour'
            ELSE locked_until
        END
    WHERE email = user_email;
END;
$function$;

-- 管理者権限チェック系
