-- ---------------------------------------------------------------------------
-- 4. RLS ポリシー（実 DB の定義を反映。auth.uid() は (select auth.uid()) でラップ済み）
-- ---------------------------------------------------------------------------

-- employees: 公開読み取り / 管理者のみ書き込み・削除
CREATE POLICY "public_employees_read" ON public.employees
    FOR SELECT USING (true);
CREATE POLICY "admin_employees_write" ON public.employees
    FOR INSERT WITH CHECK (
        (select auth.uid()) IS NOT NULL AND EXISTS (
            SELECT 1 FROM public.admin_profiles
            WHERE id = (select auth.uid()) AND is_active = true
        )
    );
CREATE POLICY "admin_employees_update" ON public.employees
    FOR UPDATE USING (
        (select auth.uid()) IS NOT NULL AND EXISTS (
            SELECT 1 FROM public.admin_profiles
            WHERE id = (select auth.uid()) AND is_active = true
        )
    );
CREATE POLICY "admin_employees_delete" ON public.employees
    FOR DELETE USING (
        (select auth.uid()) IS NOT NULL AND EXISTS (
            SELECT 1 FROM public.admin_profiles
            WHERE id = (select auth.uid()) AND is_active = true
        )
    );

-- time_records: キオスク設計（公開読み取り・追加・更新 / 管理者のみ削除）
CREATE POLICY "public_time_records_read" ON public.time_records
    FOR SELECT USING (true);
CREATE POLICY "public_time_records_insert" ON public.time_records
    FOR INSERT WITH CHECK (true);
CREATE POLICY "public_time_records_update" ON public.time_records
    FOR UPDATE USING (true);
CREATE POLICY "admin_time_records_delete" ON public.time_records
    FOR DELETE USING (
        (select auth.uid()) IS NOT NULL AND EXISTS (
            SELECT 1 FROM public.admin_profiles
            WHERE id = (select auth.uid()) AND is_active = true
        )
    );

-- admin_profiles: 自身の参照 / super_admin による作成・更新
CREATE POLICY "admin_read" ON public.admin_profiles
    FOR SELECT USING (id = (select auth.uid()));
CREATE POLICY "admin_insert" ON public.admin_profiles
    FOR INSERT WITH CHECK (
        EXISTS (
            SELECT 1 FROM public.admin_profiles admin_profiles_1
            WHERE admin_profiles_1.id = (select auth.uid())
              AND admin_profiles_1.role = 'super_admin'
              AND admin_profiles_1.is_active = true
        )
    );
CREATE POLICY "admin_update" ON public.admin_profiles
    FOR UPDATE USING (
        id = (select auth.uid()) OR EXISTS (
            SELECT 1 FROM public.admin_profiles admin_profiles_1
            WHERE admin_profiles_1.id = (select auth.uid())
              AND admin_profiles_1.role = 'super_admin'
              AND admin_profiles_1.is_active = true
        )
    );

-- audit_logs: 認証済みユーザーが追加 / 管理者が参照
CREATE POLICY "audit_logs_insert" ON public.audit_logs
    FOR INSERT WITH CHECK ((select auth.uid()) IS NOT NULL);
CREATE POLICY "audit_logs_read" ON public.audit_logs
    FOR SELECT USING (
        EXISTS (
            SELECT 1 FROM public.admin_profiles
            WHERE id = (select auth.uid()) AND is_active = true
        )
    );

