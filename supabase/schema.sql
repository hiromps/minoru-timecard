-- =====================================================================
-- 4. audit_logs — 監査ログ
-- =====================================================================
-- ⚠️重大な要突合ポイント:
--   ・トリガー関数 audit_trigger_function は old_data / new_data (json) に書き込む。
--   ・フロント (database.ts / security.ts) は old_values / new_values (+ reason,
--     ip_address, user_agent) に書き込む/読み取る。
--   両系統の列名が併存している可能性が高い。実 DB のカラム構成を必ず確認すること。
CREATE TABLE IF NOT EXISTS public.audit_logs (
    id          bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, -- ⚠️推定: TS では number
    table_name  text NOT NULL,
    record_id   text,          -- 例 "001-2026-05-27"（employee_id-record_date）または数値ID文字列
    action      text NOT NULL, -- 'INSERT' | 'UPDATE' | 'DELETE' | 'SELECT'
    old_data    jsonb,         -- audit_trigger_function が使用
    new_data    jsonb,         -- audit_trigger_function が使用
    old_values  jsonb,         -- ⚠️推定: フロントが使用（old_data と重複する可能性）
    new_values  jsonb,         -- ⚠️推定: フロントが使用（new_data と重複する可能性）
    user_id     uuid,          -- auth.uid()
    reason      text,          -- 修正理由（フロントが任意で付与。列が無い場合は new_values に格納するフォールバックあり）
    ip_address  text,          -- ⚠️推定
    user_agent  text,          -- ⚠️推定
    created_at  timestamptz NOT NULL DEFAULT now()
);

-- fix-rls-performance.sql で idx_audit_logs_user_date を削除し、こちらを残す方針
CREATE INDEX IF NOT EXISTS idx_audit_logs_user_id_created
    ON public.audit_logs (user_id, created_at);
CREATE INDEX IF NOT EXISTS idx_audit_logs_table_name
    ON public.audit_logs (table_name);

COMMENT ON TABLE public.audit_logs IS '監査ログ。トリガー自動記録とフロント明示記録の両系統が書き込む。';

